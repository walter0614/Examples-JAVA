    private void BtGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtGrabarActionPerformed
        String Nombre=TxNombre.getText();      
        if(Nombre.length()==0)
        {
            JOptionPane.showMessageDialog(null,"Ingrese el Nombre");
        }
        else
        {
            String Tipo=(String) CbFilas.getSelectedItem();
            if(Tipo.length()==0)
            {
                JOptionPane.showMessageDialog(null,"Seleccione el Tipo de Cliente");
            }
            else{
                Controlador obj=new Controlador();
                obj.AgregarCliente(Nombre, Tipo);
                dispose();
                FormularioPrincipal Fi=new FormularioPrincipal();
                Fi.show(); 
            }
        }
    }//GEN-LAST:event_BtGrabarActionPerformed

